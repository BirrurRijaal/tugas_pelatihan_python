# Mengkonversi suhu Celcius ke Fahrenheit
print("\nKonversi Terperatur\n")

#Celcius
Celcius = float(input("Masukkan besar suhu Celcius :" ))
print()

#Reamur
Reamur = ((4/5) * Celcius)
print("Hasil konversi ke Reamur adalah :",Reamur,"˚Reamur")

#Fahrenheit
Fahrenheit = ((9/5) * Celcius)+32
print("Hasil konversi ke Fahrenheit adalah :",Fahrenheit,"˚Fahrenheit")

#Kelvin
Kelvin = Celcius + 273
print("Hasil konversi ke Kelvin adalah :",Kelvin,"Kelvin")